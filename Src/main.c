int periodLimitLow = 25;     //2.88 MHz
int periodLimitHigh = 60000; //144; //500  kHz
const int periodReset = 32;

const int maxPower = 128;
const int powerStep = 2;
const int powerReset = 8;

int powerLimitHighFrac = 50; // max fraction (x/100) of the duty cycle to be high

volatile int period = periodReset;
volatile int volume = powerReset;
unsigned int buttonInput = 0xffffffff;
unsigned int oldButtonInput = 0xffffffff;
	
void handleButtons(void){
	buttonInput = GPIOB->IDR;
	oldButtonInput = buttonInput^oldButtonInput; //get change in buttons
	
	if(oldButtonInput & GPIO_PIN_9){ //if difference on left bottom button
		if(buttonInput & GPIO_PIN_9){ //if released
			
		} else { //pressed
			period -= 1;
		}
	}
	
	if(oldButtonInput & GPIO_PIN_8){ //if difference on left center button
		if(buttonInput & GPIO_PIN_8){ //if released
			
		} else { //pressed
			period = periodReset;
		}
	}
	
	if(oldButtonInput & GPIO_PIN_7){ //if difference on left top button
		if(buttonInput & GPIO_PIN_7){ //if released
			
		} else { //pressed
			period += 1;
		}
	}
	
	if(oldButtonInput & GPIO_PIN_6){ //if difference on left bottom button
		if(buttonInput & GPIO_PIN_6){ //if released
			
		} else { //pressed
			if(volume - powerStep > 0){volume -= powerStep;}
			else{volume = 0;}
		}
	}
	
	if(oldButtonInput & GPIO_PIN_4){ //if difference on left center button
		if(buttonInput & GPIO_PIN_4){ //if released
			
		} else { //pressed
			volume = powerReset;
		}
	}
	
	if(oldButtonInput & GPIO_PIN_3){ //if difference on left top button
		if(buttonInput & GPIO_PIN_3){ //if released
			
		} else { //pressed
			if(volume+powerStep <= maxPower){volume += powerStep;}
			else{volume = maxPower;}
		}
	}
		
	oldButtonInput = buttonInput;
	
	//limit period
	if(period < periodLimitLow){period = periodLimitLow;}
	else if(period > periodLimitHigh){period = periodLimitHigh;}
	
	//set period and power in stm32f1xx_it
	TIM2->ARR = period;
	TIM2->CCR1 = period/2;
}


